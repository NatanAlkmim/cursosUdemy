const a = 7
let b = 3

b += a // Atribuição aditiva -> b = b + a
console.log(b)

b -= 4 // adição subtrativa -> b = b - a
console.log(b)

b *= 2 // atribuição multiplicativa -> b = b * a
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // b = b % 2 -> resto da divisão do valor de b com o módulo 2
console.log(b)