// Antes do ES7
let exp1 = Math.pow(2, 3); // O primeiro parâmetro é a base e o segundo o exponente

console.log(exp1);

// Depois do ES7
let exp2 = 2 ** 3; // O primeiro valor representa a base e o segundo o expoente

console.log(exp2);

// Atribuição exponencial
let base1 = 2;
base1 **= 3; // Eleva o valor armazenado na base à terceira potência
//base = base ** 3
console.log(base1);

let exp3 = 2 ** -3; //Com expoente negativo

console.log(exp3)

let base2 = 2
base2 **= -3

console.log(base2)